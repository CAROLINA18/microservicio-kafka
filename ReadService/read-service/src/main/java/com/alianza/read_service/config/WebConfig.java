package com.alianza.read_service.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.CorsRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class WebConfig {

    @Bean
    public WebMvcConfigurer corsConfigurer() {
        return new WebMvcConfigurer() {
            @Override
            public void addCorsMappings(CorsRegistry registry) {
                registry.addMapping("/**") // Permitir todos los endpoints
                        .allowedOrigins("http://localhost:4200") // Permitir solicitudes desde Angular
                        .allowedMethods("GET", "POST", "PUT", "DELETE", "OPTIONS") // MÃ©todos permitidos
                        .allowedHeaders("*") // Permitir todos los encabezados
                        .exposedHeaders("Authorization") // Exponer encabezados personalizados como Authorization
                        .allowCredentials(true); // Permitir cookies o credenciales si es necesario
            }
        };
    }
}
